package fineDust;

public class FineDust {

	private String fineDust;
	private String day;
	private String temperature;
	private String humidity;
	private int numberbbs;
	private String imagekm;
	private String imagedb;

	

	public String getImagedb() {
		return imagedb;
	}

	public void setImagedb(String imagedb) {
		this.imagedb = imagedb;
	}

	public String getImagekm() {
		return imagekm;
	}

	public void setImagekm(String imagekm) {
		this.imagekm = imagekm;
	}

	public int getNumberbbs() {
		return numberbbs;
	}

	public void setNumberbbs(int numberbbs) {
		this.numberbbs = numberbbs;
	}

	public String getFineDust() {
		return fineDust;
	}

	public void setFineDust(String fineDust) {
		this.fineDust = fineDust;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getTemperature() {
		return temperature;
	}

	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}

	public String getHumidity() {
		return humidity;
	}

	public void setHumidity(String humidity) {
		this.humidity = humidity;
	}

}
